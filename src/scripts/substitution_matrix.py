def get_substitution_score(i, j, matrix) -> int:
    """
    Get the substitution score for two amino acids.
    """
    return matrix[__INDEX[i]][__INDEX[j]]


def get_matrix(matrix_name: str) -> list[list[int]]:
    """
    Get a substitution matrix by name.
    """
    return __MATRICES[matrix_name.upper()]


